    lines.append('                value:')
    lines.append('                  jsonrpc: "2.0"')
    lines.append('                  method: tools/call')
    lines.append('                  params:')
    lines.append('                    name: send_whatsapp_message')
    lines.append('                    arguments:')
    lines.append('                      phone: "+34600000000"')
