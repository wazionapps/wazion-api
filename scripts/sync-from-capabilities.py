def main():
    if len(sys.argv) < 2:
        print(f"Usage: {sys.argv[0]} <capabilities.json>", file=sys.stderr)
        sys.exit(1)

    caps = load_capabilities(sys.argv[1])

    # Generate tools catalog
    catalog = generate_tools_catalog(caps)
    catalog_path = os.path.join(REPO_ROOT, 'docs', 'tools-catalog.md')
    with open(catalog_path, 'w', encoding='utf-8') as f:
        f.write(catalog)
    print(f"✓ Generated {catalog_path}")

    # Generate partial OpenAPI
    openapi = generate_openapi_yaml(caps)
    openapi_path = os.path.join(REPO_ROOT, 'website', 'static', 'openapi.yaml')
    with open(openapi_path, 'w', encoding='utf-8') as f:
        f.write(openapi)
    print(f"✓ Generated {openapi_path}")

    # Summary
    actions = caps.get('actions', {})
    visible = {k: v for k, v in actions.items() if not v.get('mcp_hidden', False)}
    print(f"  Tools: {len(visible)} (v{caps.get('version', '?')})")


if __name__ == '__main__':
    main()
