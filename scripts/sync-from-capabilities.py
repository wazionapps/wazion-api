#!/usr/bin/env python3
"""
Sync developers.wazion.com content from WAzion capabilities.json.

Generates:
  - docs/tools-catalog.md (full tools table)
  - website/static/openapi.yaml (partial MCP OpenAPI spec)

Usage:
  python scripts/sync-from-capabilities.py <path-to-capabilities.json>
  python scripts/sync-from-capabilities.py -  # reads from stdin
"""

import json
import sys
import os
from datetime import datetime, timezone

REPO_ROOT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))


def load_capabilities(path):
    if path == '-':
        return json.load(sys.stdin)
    with open(path, 'r', encoding='utf-8') as f:
        return json.load(f)


def generate_tools_catalog(caps):
    """Generate docs/tools-catalog.md from capabilities.json."""
    actions = caps.get('actions', {})
    categories = caps.get('categories', {})
    version = caps.get('version', '?')

    # Filter out hidden tools
    visible = {k: v for k, v in actions.items() if not v.get('mcp_hidden', False)}

    # Sort alphabetically by action name
    sorted_actions = sorted(visible.items(), key=lambda x: x[0])

    lines = []
    lines.append('<!-- AUTO-GENERATED-START — Do not edit between these markers. Content is regenerated by sync-public-docs workflow. -->')
    lines.append(f'# WAzion Tools Catalog')
    lines.append('')
    lines.append(f'> Auto-generated from `capabilities.json` v{version}')
    lines.append(f'> Total tools: {len(sorted_actions)} (excluding hidden)')
    lines.append('')
    lines.append('| Tool | Category | Type | Description |')
    lines.append('|------|----------|------|-------------|')

    for name, action in sorted_actions:
        cat_key = action.get('category', 'general')
        cat_name = categories.get(cat_key, {}).get('name', cat_key)
        action_type = action.get('type', 'query')
        desc = action.get('description', '')
        # Truncate long descriptions for table readability
        if len(desc) > 120:
            desc = desc[:117] + '...'
        # Escape pipe characters in description
        desc = desc.replace('|', '\\|')
        lines.append(f'| `{name}` | {cat_name} | {action_type} | {desc} |')

    lines.append('')
    lines.append('---')
    lines.append('')
    lines.append('See [OpenAPI spec](../openapi.yaml) for full parameter details.')
    lines.append('<!-- AUTO-GENERATED-END -->')
    lines.append('')

    return '\n'.join(lines)


def generate_openapi_yaml(caps):
    """Generate website/static/openapi.yaml (partial) from capabilities.json."""
    actions = caps.get('actions', {})
    categories = caps.get('categories', {})
    version = caps.get('version', '?')

    visible = {k: v for k, v in actions.items() if not v.get('mcp_hidden', False)}

    # Build tags from categories
    tag_lines = []
    for key, cat in sorted(categories.items()):
        tag_lines.append(f'  - name: {key}')
        tag_lines.append(f'    description: {cat.get("description", cat.get("name", key))}')

    # Build tool schemas for each action
    tool_schemas = []
    for name, action in sorted(visible.items()):
        cat = action.get('category', 'general')
        desc = action.get('description', '')
        action_type = action.get('type', 'query')
        params = action.get('params', {})

        # Build parameters schema
        props = {}
        required = []
        if isinstance(params, dict):
            for pname, pdef in params.items():
                prop = {}
                prop['type'] = pdef.get('type', 'string')
                if 'description' in pdef:
                    prop['description'] = pdef['description']
                if 'enum' in pdef:
                    prop['enum'] = pdef['enum']
                if 'default' in pdef:
                    prop['default'] = pdef['default']
                props[pname] = prop
                if pdef.get('required', False):
                    required.append(pname)

        tool_schemas.append({
            'name': name,
            'category': cat,
            'description': desc,
            'type': action_type,
            'properties': props,
            'required': required,
            'confirmation': action.get('confirmation_required', False),
        })

    # Generate YAML manually (no pyyaml dependency needed)
    lines = []
    lines.append('openapi: 3.1.0')
    lines.append('info:')
    lines.append('  title: WAzion API')
    lines.append('  description: |')
    lines.append('    WAzion is a WhatsApp Business automation and AI assistant platform. This API exposes')
    lines.append(f'    {len(visible)} tools via the Model Context Protocol (MCP) over JSON-RPC 2.0, allowing you to')
    lines.append('    manage every aspect of your WAzion dashboard programmatically.')
    lines.append('')
    lines.append('    ## Protocol')
    lines.append('')
    lines.append('    All requests use **JSON-RPC 2.0** over a single POST endpoint. The server implements')
    lines.append('    the MCP Streamable HTTP transport (spec 2025-03-26).')
    lines.append('')
    lines.append('    ## Authentication')
    lines.append('')
    lines.append('    Include your API token as a Bearer token in the `Authorization` header. You can find')
    lines.append('    your token in the WAzion Dashboard under **Settings**.')
    lines.append('')
    lines.append('    ## Quick Start')
    lines.append('')
    lines.append('    ```bash')
    lines.append("    curl -X POST https://www.wazion.com/api/mcp/ \\")
    lines.append('      -H "Content-Type: application/json" \\')
    lines.append('      -H "Authorization: Bearer YOUR_TOKEN" \\')
    lines.append("      -d '{\"jsonrpc\":\"2.0\",\"method\":\"tools/call\",\"params\":{\"name\":\"get_shop_status\",\"arguments\":{}},\"id\":1}'")
    lines.append('    ```')
    lines.append(f'  version: {version}')
    lines.append('  contact:')
    lines.append('    name: WAzion Support')
    lines.append('    url: https://www.wazion.com')
    lines.append('    email: support@wazion.com')
    lines.append('  license:')
    lines.append('    name: MIT')
    lines.append('    url: https://opensource.org/licenses/MIT')
    lines.append('')
    lines.append('servers:')
    lines.append('  - url: https://www.wazion.com/api/mcp')
    lines.append('    description: Production')
    lines.append('')
    lines.append('security:')
    lines.append('  - bearerAuth: []')
    lines.append('')
    lines.append('tags:')
    for tl in tag_lines:
        lines.append(tl)

    # Paths section — single MCP endpoint
    lines.append('')
    lines.append('paths:')
    lines.append('  /:')
    lines.append('    post:')
    lines.append('      operationId: jsonRpcRequest')
    lines.append('      summary: JSON-RPC 2.0 endpoint')
    lines.append('      description: |')
    lines.append('        All WAzion API operations go through this single endpoint using JSON-RPC 2.0.')
    lines.append('')
    lines.append('        ### Available methods')
    lines.append('')
    lines.append('        | Method | Auth Required | Description |')
    lines.append('        |--------|--------------|-------------|')
    lines.append('        | `initialize` | No | MCP handshake |')
    lines.append('        | `tools/list` | Yes | List all available tools |')
    lines.append('        | `tools/call` | Yes | Execute a tool |')
    lines.append('        | `resources/list` | Yes | List available resources |')
    lines.append('        | `resources/read` | Yes | Read a resource (e.g. shop://profile) |')
    lines.append('        | `ping` | Yes | Health check |')
    lines.append('')
    lines.append('        ### Tool execution')
    lines.append('')
    lines.append('        Use `tools/call` with `params.name` set to the tool name and `params.arguments`')
    lines.append('        containing the tool parameters.')
    lines.append('')
    lines.append('        ### Confirmations')
    lines.append('')
    lines.append('        Some destructive actions require confirmation. On the first call, the server')
    lines.append('        returns a warning message. Call again with `confirm: true` in the arguments')
    lines.append('        to execute.')
    lines.append('')
    lines.append('        ### Batch requests')
