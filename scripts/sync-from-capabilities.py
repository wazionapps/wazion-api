    lines.append('              oneOf:')
    lines.append("                - $ref: '#/components/schemas/JsonRpcRequest'")
    lines.append('                - type: array')
    lines.append('                  items:')
    lines.append("                    $ref: '#/components/schemas/JsonRpcRequest'")
    lines.append('            examples:')
